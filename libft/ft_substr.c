#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*tmp;
	size_t	slen;

	if (!s)
		return (NULL);
	slen = ft_strlen(s);
	if (start >= slen)
		return (ft_strdup(""));
	if (len > slen - start)
		len = slen - start;
	tmp = malloc(sizeof(char) * (len + 1));
	if (!tmp)
		return (NULL);
	ft_memcpy(tmp, &s[start], len);
	tmp[len] = '\0';
	return (tmp);
}
